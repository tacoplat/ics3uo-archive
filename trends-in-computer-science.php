					<h2>Virtual and Augmented Reality</h2>
					<img class="image right" src="https://upload.wikimedia.org/wikipedia/commons/5/5f/Augmented-reality.jpg" alt="A picture of an Augmented Reality App in action, inserting furniture into the real world." title="Augmented Reality App">
					<p class="pgraph"><h3>The Difference Between Virtual Reality and Augmented Reality</h3>What is the difference between Virtual Reality (VR) and Augmented Reality (AR)? Well, to begin, virtual reality is an artificial simulation of an experience. This could refer to a simulation of a real-world event or atmosphere. When immersed in virtual reality, the user is made to feel like they are actually in the simulated environment, through the manipulation of at least one of the five senses. Virtual reality can be accesed through a wearable device, like a headset. Meanwhile, augmented reality is the enhancement of any existing scenario with the use of computer-aided overlays and enhancements. An example would be a mobile game with an additional layer of reality, like Pokemon GO and the AR camera filter.</p>
					<p class="pgraph"><h3>Applications of VR and AR</h3>What can virtual and augmented reality be used for? Currently, these two technologies are being used for a multitude of purposes. From entertainment to occupation training, or even navigation, there is no doubt that VR and AR are trending. To elaborate, they can be used in video game experiences, media entertainment, in situations where danger is present, to test new colours for your home decor, insert furniture into your living room, test makeup, or even plan surgeries.</p>
