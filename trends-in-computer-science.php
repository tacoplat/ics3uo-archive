					<h2>Big Data</h2>
					<img class="image left" src="https://upload.wikimedia.org/wikipedia/commons/5/57/Bar_graph_-_stock_price_statistics.jpg" alt="A bar chart, to represent data in general." title="Bar Graph">
					<p class="pgraph"><h3>Big Data?</h3>Big Data refers to the massive amounts of data and information available in the world, in the form of data sets. It's useful to innovative corporations to indicate trends and allow for improvements for both the organization and individual consumer.</p>
					<p class="pgraph"><h3>Why is it useful?</h3>From the vast amounts of data out there, we can search through it all and determine trends and patterns in several factors that have the potential to affect the success of something. We can investigate trends in regards to innovation, competition, and productivity growth. For example, a company can hire an analyst to pinpoint trends that they can manipulate to improve their products or services. Big Data can also be used to recommend services to customers, or even to aid in problem-solving.</p>
					<p class="pgraph"><h3>Sources of Big Data</h3>Big Data is a unthinkably large concept, which can be obtained from almost anywhere. Big Data comes from Facebook analytics, consumer surveys, experimental trials, log entries, call logs, you name it.</p>
					<p class="pgraph"><h3>How are companies applying Big Data?</h3>Apart from the examples mentioned above, Big Data has so many uses. The Oracle website suggests several: fraud detection through identifying patterns in service usage, machine learning, where machines use pre-existing knowledge and build on it, or even to increase operational efficiency.</p>
					<p class="pgraph"><h3>An Anecdote</h3>So, last year (the 2018-2019 school year), I was provided the opportunity to partake in the "Big Data Challenge", operated by the STEM Fellowship. Of course, the goal was to develop an innovative solution to a prevalent problem within our society, through the analysis of Big Data. That being said, I had absolutely no clue on what to do. I personally felt as if Big Data was like the ocean, and I was a sailor stranded within it. I'm sure with adequate training, one can make some <i>rad</i> contributions to society, but as of last year, it just wasn't right for me.</p>
