					<h2>Cloud Computing</h2>
					<img class="image left" src="https://www.fastmetrics.com/blog/wp-content/uploads/2014/10/cloud-computing-service-types-including-IoT-1024x706.png" alt="A diagram of the cloud, showing the connections between the services and the cloud." title="Cloud Diagram">
					<p class="pgraph"><h3>What is Cloud Computing?</h3><u>Cloud computing</u> is the transmission of "computing services" over the internet. There are a multitude of services that can be offered as a part of cloud computing:</p>
					<ul>
						<li>Office applications</li>
						<li>Professional tools</li>
						<li>Processing power</li>
						<li>Virtual Machine access</li>
						<li>Storage</li>
					</ul>
					<p class="pgraph">Cloud computing usually operates as a renting or subscription-based service, where the customer pays a set amount every billing term in exchance for access to the desired service. For the most part, there are three main service models in cloud-based computing: Infrastructure-as-a-Service (IaaS), Platform-as-a-Service (PaaS), and Software-as-a-Service (Saas).</p>
					<div class="videowrap">
						<iframe width="560" height="315" src="https://www.youtube.com/embed/uYGQcmZUTaw" frameborder="0" allow="accelerometer; autoplay; encrypted-media; gyroscope; picture-in-picture" allowfullscreen class="video"></iframe>
					</div>
					<p class="pgraph"><h3>IaaS, PaaS, and SaaS</h3><u>Infrastructure-as-a-Service</u> refers to the purchase of services required to develop things from scratch. Ex: servers and storage.<br><u>Platform-as-a-Service</u> models are similar to IaaS models, although they also include tools and utilities to aid in the development of applications and such. Ex: web hosting with database management.<br><u>Software-as-a-Service</u> provides users with whole applications to conduct their day-to-day business. Ex: photo-editing apps and word processors.</p>
					<p class="pgraph"><h3>Advantages and Disadvantages</h3>The "Cloud" is beneficial to a variety of people. It allows customers to purchase services according to their needs; If a large corporation needs access to a server, they don't have to buy and maintain one by themselves. Conversely, for personal usage, cloud computing allows individuals to essentially "rent" licenses or access for program suites, like Adobe Creative Cloud or Microsoft Office 365, instead of shelling out large amounts for program packages or individual applications. In short, cloud-based services are flexible in nature.<br>One disadvantage to cloud services is the cost. Since cloud services are based on a rental plan, in the long term, one could end up paying much more than say, if they only needed lifetime access to one specific service.</p>
					<p class="pgraph"><h3>Real-life Examples of Cloud Services</h3>The following are all actual examples of cloud computing in action, that you may use on a day-to-day basis: Microsoft Office 365, Adobe Creative Cloud, Google Drive (and Google Suite), Google Cloud Platform, Microsoft Azure, Amazon AWS, and Netflix.</p>
