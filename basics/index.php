	<strong><h3>Diet</h3></strong>
	<p class="fancy"> The platypus' diet consists of small invertabrates like worms, crayfish, shrimp, insect larvae, etc... To catch their food, the platypus dives to the bottom of the water body, using its bill to search for food. Interestingly enough, platypuses cannot see underwater, so they must use "electro-magnetic receptors" in their bills to sense their prey. 
