	<strong><h3>Fun Facts</h3></strong>
		<div class="fancy">
		<ul>
			<li>Platypuses do not have teeth. They use "grinding pads" in their mouths to consume their food.</li>
			<li>Platypuses appear in approximately eighty percent of river basins in Victoria, Australia.</li>
			<li>Technically, the correct plural of the word "platypus" is "platypuses", even though many people tend to use the word "platypi".</li>
			<li>When the female platypus lactates, the milk comes out like sweat.</li>
