	<strong><h3>Description and Overview</h3></strong>
	<p class="fancy">The <b>Duck-billed Platypus</b> (scientific name <i>Ornithorhynchus anatinus</i>), is an interesting Australian amphibious land mammal. It is known as a confusing creature in its anatomy; It lays eggs, yet it also produces milk for its offspring. It has a bill, but it also has fur. It has webbed feet. It looks like a mix between a beaver, duck, and mole. Male platypuses have a poisonous barb on their hind legs. The platypus is truly a strange creature, isn't it?</p>
	<strong><h3>Habitat</h3></strong>
	<p class="fancy">Platypuses are native to <a href="https://www.australia.gov.au/">Australia</a>. Typically, wild platypuses reside in bodies of freshwater (like lakes and streams) within certain regions of Australia, like Tasmania, Eastern Queensland and New South Wales. To be more specific, platypuses and their young inhabit the burrows they create.
