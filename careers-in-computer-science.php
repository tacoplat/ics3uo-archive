					<h2>Post-secondary Pathways and Programs</h2>
					<p class="pgraph"><h3>University of Waterloo</h3>Programs: <a href="https://uwaterloo.ca/future-students/programs/computer-science">Computer Science (B.Cs, B.Math)</a>, <a href="https://uwaterloo.ca/future-students/programs/computer-engineering">Electrical/Computer Engineering (B.A.Sc.)</a>, <a href="https://uwaterloo.ca/future-students/programs/data-science">Data Science (B.Cs, B.Math)</a>, <a href="https://uwaterloo.ca/future-students/programs/mechatronics-engineering">Mechatronics Engineering (B.A.Sc.)</a><br>Summary: The University of Waterloo offers an assortment of competitive programs with elements of computer science and engineering. These programs typically come with a co-op placement, allowing students to obtain paid work experience. Personally, I'm interested in applying for their Mechatronics Engineering program, which combines both physical/mechanical features with some programming.<br>Admissions: Completion of the Ontario Secondary School Diploma. For most of these programs, some combination of SPH4U, MHF4U, MCV4U, SCH4U and ENG4U are required, with at least another U/M level course or two to fufill the 6-course sample. Additionally, a well-written Admissions Information Form and optional video interview may be beneficial.</p>
					<p class="pgraph"><h3>McMaster University</h3>Programs: <a href="https://future.mcmaster.ca/programs/compsci/">Computer Science (B.A.Sc.)</a>, <a href="https://future.mcmaster.ca/programs/engineering/">Engineering I (B.Eng, B.Eng.Mgt, B.Eng.Society, B.Eng.Biosci.)</a><br>Summary: For McMaster's computer science program, offered by their Faculty of Engineering, students undertake a four-year undergraduate program, learning concepts and the theory of computer science. The Engineering I is a general program that branches off into more specific disciplines later. A co-op option is optional.<br>Admissions: McMaster's CS program requires the completion of the Ontario Secondary School Diploma, along with ENG4U, MCV4U, and two of (SBI4U, SCH4U, SPH4U, SES4U, ICS4U, or TEJ4M). The Engineering program requires ENG4U, MCV4U, SCH4U and SPH4U.</p>
					<p class="pgraph"><h3>Sheridan College</h3>Programs: <a href="https://academics.sheridancollege.ca/programs/computer-programmer">Computer Programmer (H.B.A.Sc, H.B.A.ISc), <a href="https://academics.sheridancollege.ca/programs/computer-systems-technician-software-engineering">Computer Systems Technician - Software Engineering (H.B.A.Sc, H.B.A.ISc)</a>, <a href="https://academics.sheridancollege.ca/programs/computer-engineering-technician-technology">Computer Engineering Technician/Technology (H.B.A.Sc, H.B.A.ISc)</a><br>Summary: Sheridan offers an assortment of programs related to computers. Within these programs, students develop high-level programming skills that can be directly applie to the workplace. Second-year students have the opportunity to partake in co-op opportunities.<br>Admissions: OSSD, ENG4C/4U, Any Grade 12 Math C/U or MCF3M/MCR3U.</p>
					<p class="pgraph"><h3>University of Toronto</h3>Programs: <a href="https://future.utoronto.ca/undergraduate-programs/computer-science/">Computer Science (H.B.Sci)</a><br>Summary: UofT's computer science program can be taken at any of their three campuses (St. George, Mississauga, Scarborough). Students explore the theory and applications of computer science. The university offers several other related programs, but I have decided not to list them due to the sheer amount.<br>Admissions: Completion of the OSSD, MCV4U, ENG4U, and 4 other U/M level courses.</p>
					<p class="pgraph"><h3>Fanshawe College</h3>Programs: <a href="https://www.fanshawec.ca/programs/cty2-computer-systems-technology/next">Computer Systems Technology</a>, <a href="https://www.fanshawec.ca/programs/cpa3-computer-programming-and-analysis-20202021/next">Computer Programming and Analysis</a><br>Summary: Fanshawe offers a couple of interesting computer-oriented courses, as well. Students have the opportunity to work 12 months of paid co-ops. They also are able to earn several certifications that may translate into the real workplace.<br>Admissions: OSSD, Any Grade 12 English C/U, Any Grade 11 or 12 Mathematics C/U/M, or Academic and Career Entrance Certificate, or others.</p>
