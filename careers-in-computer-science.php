					<h2>What is Computer Science?</h2>
					<p class="pgraph"><i>Computer Science</i> is the broad sector involving both the theory and application of computers. Computer science encompasses the transmission and storage of information, as well as analyzing data to produce better products.</p>
				</div>
				<div class="section">
					<h2>Possible Careers in Computer Science</h2>
					<p class="pgraph">What can you do with adequate computer science training? There's a wide range of positions for you; Companies like Apple, Nvidia, Google and Microsoft are always in demand for these sorts of jobs. Here is a general list of positions that you can obtain upon becoming proficient in computer science topics:</p>
					<img src="https://hackernoon.com/hn-images/1*9npNPVH7iNJ64Koq7EcW5A.jpeg" class="image right">
					<p class="pgraph"><b><u>Full-stack Developer</u></b><br>Approximate annual salary range (CAD): $60,000 - $180,000<br>A Full-stack developer is someone who specializes in both front-end and back-end development, meaning they are equally competent in the things you see and can't see, usually in web development. They produce work that features a user interface, interlocked with systems to manage data and website functions. Full-stack developers are expected to be creative, analytical, and detail-oriented, as well as to have background in both front-end languages (HTML, CSS) and back-end languages (SQL, JAVA). <a href="https://jobs.scotiabank.com/job/Toronto-Full-Stack-%28React%2C-Node-JS%29-Software-Developer%2C-Digital-Factory%2C-%286-12-Month-contract%29-ON/535973217/?feedId=276917&utm_campaign=Scotiabank_ST&utm_source=Indeed">See an example here</a></p>
					<p class="pgraph"><b><u>Research Assistant</u></b><br>Approximate annual salary range (CAD): $25,000 - $100,000<br>Research Assistants are high-level researchers that conduct research in their fields. They usually have graduate degrees, and are analytic, thinker types. Typically, they work at educational institutions. <a href="https://services.iqc.uwaterloo.ca/applications/positions/rap/">See an example here</a></p>
					<p class="pgraph"><b><u>Software Engineer</u></b><br>Approximate annual salary range (CAD): $45,000 - $160,000<br>Software engineer is a broad term. They work with clients to apply engineering principles in combination with mathematical analysis to a project, based on the cleint's needs. Software engineers produce and develop software for computers, from operating systems, to games, to even network systems. Software engineers need extensive knowlege of several programming languages, as well as critical thinking skills, problem solving skills and effective communication skills. <a href="https://jobs.manulife.com/ShowJob/Id/777435/Platform-Software-Engineer/?utm_campaign=google_jobs_apply&utm_source=google_jobs_apply&utm_medium=organic">See an example here</a></p>
					<img src="https://d8it4huxumps7.cloudfront.net/bites/wp-content/uploads/2019/07/24122338/Data-Science-101.png" class="image left">
					<p class="pgraph"><b><u>Data Scientist</u></b><br>Approximate annual salary range (CAD): $30,000 - $180,000<br>Data scientists collect massive amounts of data, and transform it into readable material. They solve real-life business problems by using analytical techniques and trends among data. Data science relates to the field of computer science, through the usage of data-oriented programming languages and analysis skills. Data scientists are excellent communicators, innovative, good at spotting trends, and understand the principles of statistics and data in general. <a href="https://jobs.lever.co/loblawdigital/8503632d-aff6-43ed-87d7-2af41623d686?utm_campaign=google_jobs_apply&utm_source=google_jobs_apply&utm_medium=organic">See an example here</a></p>
					<p class="pgraph"><b><u>Video Game Developer</u></b><br>Approximate annual salary range (CAD): $40,000 - $85,000<br>Video game developers are software developers dedicated to the development of video games and entertainment systems. They are responsible for creating, designing, testing and improving games for the average consumer. Video game developers are creative, and well-immersed in programming languages. <a href="https://www.jobbank.gc.ca/jobsearch/jobposting/31076060?utm_campaign=google_jobs_apply&utm_source=google_jobs_apply&utm_medium=organic">See an example here</a></p>
